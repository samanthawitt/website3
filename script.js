target.addEventListener('drop', (e) => {
    e.preventDefault();

    const fishId = e.dataTransfer.getData('text/plain');
    
    if (!selectedFish.includes(fishId)) {
        selectedFish.push(fishId);
        localStorage.setItem('myAquarium', JSON.stringify(selectedFish));
        alert("Fish added to your bowl!");
    } else {
        alert("Fish already added to your bowl!");
    }

    target.style.display = 'none';
});

document.addEventListener('dragstart', (e) => {
    const fishContainer = e.target.closest('.fishimg-container');
    
    if (fishContainer) {
        e.dataTransfer.setData('text/plain', fishContainer.id);
        target.style.display = 'flex'; 
    }
});

document.addEventListener('dragend', () => {
    target.style.display = 'none';
});

