function saveData(fishinfo){
    localStorage.setItem('lastClickedItem', JSON.stringify(fishinfo));
}

function updateAside() {
    const fishname = document.querySelector('.fishiename');
    const fishieinfo = document.querySelector('.fishieinfo');
    const savedData = localStorage.getItem('lastClickedItem');

    if (savedData) {
        const lastfish = JSON.parse(savedData);
        fishname.textContent = lastfish.name;
        fishieinfo.textContent = lastfish.info;
    }
    else {
        fishname.textContent = "Click on a fish to learn more!"; }
}

