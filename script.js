async function getWeather(city) {
    try {
        let geoResponse = await fetch(`https://cse2004.com/api/geocode?address=${city}`)
        let geoData = await geoResponse.json()

        let lat = geoData.results[0].geometry.location.lat
        let lng = geoData.results[0].geometry.location.lng

        let weatherResponse = await fetch(`https://cse2004.com/api/weather?latitude=${lat}&longitude=${lng}`)
        let weather = await weatherResponse.json()
        return weather
    } catch (error) {
        console.error("Weather fetch failed:", error);
        return null;
    }
}

document.querySelectorAll('.fish').forEach(fish => {
    const imgContainer = fish.querySelector('.fishimg-container');
    const infoBox = fish.querySelector('.fishinfo');
    const weatherText = fish.querySelector('.fish-weather');
    const city = fish.dataset.city; 
    const thisinfobutton = fish.querySelector('.infobutton')
    const deletebutton = fish.querySelector('.fishbowldelete'); 

    thisinfobutton.addEventListener('click', (e) => {
        e.stopPropagation();
        information.classList.toggle('informed');
        if (information.classList.contains('informed')) {
            information.style.display = 'block';
            updateAside();
        } else{
            information.style.display = 'none';
        }
        });

    imgContainer.addEventListener('click', async (e) => {
        e.stopPropagation();
        const isAlreadyClicked = fish.classList.contains('clicked');
        document.querySelectorAll('.fish').forEach(f => {
            f.classList.remove('clicked');
            f.querySelector('.fishinfo').style.display = 'none';
        });

        if (!isAlreadyClicked) {
            fish.classList.add('clicked');
            
            document.body.classList.add('fish-is-active');

            const weather = await getWeather(city);
            if (weather) {
                weatherText.textContent = `Current temp in this fish's habitat: ${weather.feelsLikeTemperature.degrees}°`;
            } else {
                weatherText.textContent = 'Current weather information is currently unavailable.';
            }

            display(fish, imgContainer, infoBox);
            const dataToSave = JSON.parse(fish.dataset.fishData);
            saveData(dataToSave);

            if (information.classList.contains('informed')) {
                updateAside();
            }
        } else {
            document.body.classList.remove('fish-is-active');
        }

        if (information.classList.contains('informed')) {
                thisinfobutton.style.display = 'none';
                updateAside();
            } else {
                thisinfobutton.style.display = 'block';
            }
    });

    deletebutton.addEventListener('click', (e) => {
        e.stopPropagation(); // Prevent the fish from "clicking" behind the button
        const fishId = imgContainer.id;
        selectedFish = selectedFish.filter(id => id !== fishId);
        localStorage.setItem('myAquarium', JSON.stringify(selectedFish));
        imgContainer.style.display = 'none';
        console.log(`${fishId} removed from bowl.`);
    });

});

function display(fish, fishimgcontainer, fishinfo) {
    if (fish.classList.contains('clicked')) {
        fishinfo.style.display = 'block';
        const rect = fishimgcontainer.getBoundingClientRect();
        const infoHeight = fishinfo.offsetHeight;
        const spacing = 50;

        let left = (rect.left + window.scrollX + (rect.width / 2)) - (fishinfo.offsetWidth / 2);
        
        if (left + fishinfo.offsetWidth > window.innerWidth) {
            left = window.innerWidth - (fishinfo.offsetWidth + 20);
        }
        if (left < 0) left = 10; 

        const spaceBelow = window.innerHeight - rect.bottom;
        let top;

        if (spaceBelow < infoHeight + spacing) {
            top = rect.top + window.scrollY - infoHeight - spacing;
        } else {
            top = rect.bottom + window.scrollY + spacing;
        }

        fishinfo.style.top = `${top}px`;
        fishinfo.style.left = `${left}px`;
    } else {
        fishinfo.style.display = 'none';
    }
}

